package MathUtils;

public class MathUtils {
    public int add(int a, int b) {
        return a + b;
    }

    public long sub(long a, long b) {
        return a - b;
    }

    public double mult(double a, double b) {
        return a * b;
    }

    public double Div(double a, int b) {
        return a / b;

    }
}
